import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import * as cheerio from 'cheerio';
import {
  TOPIC_PAGES,
  summarizeAnswer,
  buildChoices,
  parseLevelQuestions,
  parseInlineQuizzes,
} from '../tools/quiz-bank.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const APP_ROOT = path.join(__dirname, '..');
const REPO_ROOT = path.join(APP_ROOT, '..');
const PAGES_DIR = path.join(REPO_ROOT, 'agent_context_academy', 'pages');
const OUT_FILE = path.join(REPO_ROOT, 'agent_context_academy', 'assets', 'question-bank.json');
const OUT_JS = path.join(REPO_ROOT, 'agent_context_academy', 'assets', 'question-bank.data.js');

/** @type {{ topics: object[], allSummaries: string[] }} */
const bank = { topics: [], allSummaries: [] };

/** @type {Map<string, string[]>} */
const topicAnswerPools = new Map();

for (const topic of TOPIC_PAGES) {
  const filePath = path.join(PAGES_DIR, topic.file);
  if (!fs.existsSync(filePath)) {
    console.warn('Skip missing:', topic.file);
    continue;
  }
  const html = fs.readFileSync(filePath, 'utf8');
  const $ = cheerio.load(html);
  const levelQs = parseLevelQuestions($);
  const summaries = levelQs.map((q) => summarizeAnswer(q.answer));
  topicAnswerPools.set(topic.id, summaries);
  bank.allSummaries.push(...summaries);
}

const globalPool = [...new Set(bank.allSummaries)];

for (const topic of TOPIC_PAGES) {
  const filePath = path.join(PAGES_DIR, topic.file);
  if (!fs.existsSync(filePath)) continue;

  const html = fs.readFileSync(filePath, 'utf8');
  const $ = cheerio.load(html);
  const questions = [];
  let idx = 0;

  const levelQs = parseLevelQuestions($);
  const localPool = topicAnswerPools.get(topic.id) || [];
  const poolForDistractors = [...new Set([...localPool, ...globalPool])];

  for (const row of levelQs) {
    idx += 1;
    const suffix = String(idx).padStart(2, '0');
    const id = `${topic.id}-${row.level.slice(0, 1)}${suffix}`;
    const others = poolForDistractors.filter(
      (s) => s.toLowerCase() !== summarizeAnswer(row.answer).toLowerCase()
    );
    questions.push({
      id,
      level: row.level,
      question: row.question,
      answer: row.answer,
      choices: buildChoices(id, row.answer, others),
      choicesSource: 'heuristic',
    });
  }

  const inline = parseInlineQuizzes($);
  for (const q of inline) {
    idx += 1;
    const id = `${topic.id}-quiz-${String(idx).padStart(2, '0')}`;
    const correct = q.choices.find((c) => c.correct);
    const answer =
      correct?.text ||
      q.choices
        .filter((c) => c.correct)
        .map((c) => c.text)
        .join('; ') ||
      '';
    questions.push({
      id,
      level: 'middle',
      question: q.question,
      answer: answer || q.question,
      choices: q.choices.length >= 2 ? q.choices : buildChoices(id, answer, poolForDistractors),
      choicesSource: q.choicesSource || 'html',
    });
  }

  if (questions.length === 0) continue;

  bank.topics.push({
    id: topic.id,
    title: topic.title,
    page: `pages/${topic.file}`,
    questionCount: questions.length,
    questions,
  });
}

const totalQuestions = bank.topics.reduce((n, t) => n + t.questions.length, 0);
const output = {
  version: 1,
  generatedAt: new Date().toISOString(),
  totalQuestions,
  topics: bank.topics,
};

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, JSON.stringify(output, null, 2) + '\n', 'utf8');
const jsBody =
  '/* generated by npm run build:bank — для открытия квиза через file:// */\n' +
  'window.ACADEMY_QUESTION_BANK = ' +
  JSON.stringify(output) +
  ';\n';
fs.writeFileSync(OUT_JS, jsBody, 'utf8');
console.log(`Wrote ${totalQuestions} questions in ${bank.topics.length} topics → ${OUT_FILE}`);
console.log(`Wrote ${OUT_JS} (для file://)`);
