import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import * as cheerio from 'cheerio';
import { htmlToPlainText, TOPIC_PAGES } from '../tools/quiz-bank.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PAGES_DIR = path.join(__dirname, '..', '..', 'agent_context_academy', 'pages');
const ASSETS = path.join(__dirname, '..', '..', 'agent_context_academy', 'assets');
const OUT_JSON = path.join(ASSETS, 'search-index.json');
const OUT_JS = path.join(ASSETS, 'search-index.data.js');

const topicTitleByFile = new Map(TOPIC_PAGES.map((t) => [t.file, t.title]));

function pageTitle($) {
  const hero = $('section.hero h2').first().text().trim();
  if (hero) return hero;
  const t = $('title').text().trim();
  return t.replace(/\s*—.*$/, '').trim() || 'Страница';
}

function cleanSummary($, el) {
  const clone = $(el).clone();
  clone.find('.q-level').remove();
  return htmlToPlainText(clone.html() || '');
}

/** @type {{ id: string, type: string, page: string, pageTitle: string, title: string, text: string, snippet: string, level?: string, icon?: string }[]} */
const entries = [];
const seen = new Set();

function add(entry) {
  const key = `${entry.type}|${entry.page}|${entry.title}`;
  if (seen.has(key)) return;
  seen.add(key);
  entries.push(entry);
}

const files = fs
  .readdirSync(PAGES_DIR)
  .filter((f) => f.endsWith('.html'))
  .sort();

for (const file of files) {
  const html = fs.readFileSync(path.join(PAGES_DIR, file), 'utf8');
  const $ = cheerio.load(html);
  const page = `pages/${file}`;
  const pTitle = topicTitleByFile.get(file) || pageTitle($);
  const bodyText = htmlToPlainText($('main').html() || '').slice(0, 8000);

  add({
    id: `page-${file}`,
    type: 'page',
    page,
    pageTitle: pTitle,
    title: pTitle,
    text: `${pTitle} ${bodyText}`,
    snippet: $('section.hero p').first().text().trim().slice(0, 160) || pTitle,
  });

  $('section.section > h2').each((i, el) => {
    const title = htmlToPlainText($(el).html() || '').trim();
    if (!title || title.length < 2) return;
    const section = $(el).parent();
    const chunk = htmlToPlainText(section.html() || '').slice(0, 2500);
    add({
      id: `sec-${file}-${i}`,
      type: 'section',
      page,
      pageTitle: pTitle,
      title,
      text: `${pTitle} ${title} ${chunk}`,
      snippet: chunk.slice(0, 140) + (chunk.length > 140 ? '…' : ''),
    });
  });

  $('details.details').each((i, el) => {
    const title = cleanSummary($, $(el).find('summary').first());
    if (!title || title.length < 4) return;
    const inside = htmlToPlainText($(el).find('.inside').first().html() || '');
    const levelEl = $(el).find('.q-level').first();
    const level = levelEl.length
      ? levelEl.text().trim().toLowerCase()
      : undefined;
    add({
      id: `qa-${file}-${i}`,
      type: 'question',
      page,
      pageTitle: pTitle,
      title,
      text: `${title} ${inside}`,
      snippet: inside.slice(0, 160) + (inside.length > 160 ? '…' : ''),
      level,
    });
  });

  $('.nuance-card h4').each((i, el) => {
    const title = $(el).text().trim();
    const p = $(el).siblings('p').first().text().trim();
    if (!title) return;
    add({
      id: `nuance-${file}-${i}`,
      type: 'nuance',
      page,
      pageTitle: pTitle,
      title: `Тонкость: ${title}`,
      text: `${title} ${p}`,
      snippet: p.slice(0, 140),
    });
  });
}

const bankPath = path.join(ASSETS, 'question-bank.json');
if (fs.existsSync(bankPath)) {
  const bank = JSON.parse(fs.readFileSync(bankPath, 'utf8'));
  for (const topic of bank.topics || []) {
    for (const q of topic.questions || []) {
      add({
        id: `bank-${q.id}`,
        type: 'question',
        page: topic.page,
        pageTitle: topic.title,
        title: q.question,
        text: `${q.question} ${q.answer}`,
        snippet: (q.answer || '').slice(0, 160),
        level: q.level,
      });
    }
  }
}

const output = {
  version: 1,
  generatedAt: new Date().toISOString(),
  entryCount: entries.length,
  entries,
};

fs.writeFileSync(OUT_JSON, JSON.stringify(output) + '\n', 'utf8');
const jsBody =
  '/* generated by npm run build:search — для file:// */\n' +
  'window.ACADEMY_SEARCH_INDEX = ' +
  JSON.stringify(output) +
  ';\n';
fs.writeFileSync(OUT_JS, jsBody, 'utf8');
console.log(`Search index: ${entries.length} entries → ${OUT_JSON}`);
